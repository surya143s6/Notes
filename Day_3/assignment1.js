var weekdays = prompt("Enter the number:");
if (weekdays == 1) {
  console.log("Monday");
} else if (weekdays == 2) {
  console.log("Tuesday");
} else if (weekdays == 3) {
  console.log("Wednesday");
} else if (weekdays == 4) {
  console.log("Thursday");
} else if (weekdays == 5) {
  console.log("Friday");
} else if (weekdays == 6) {
  console.log("Saturday");
} else if (weekdays == 7) {
  console.log("Sunday");
} else {
  console.log("not found");
}
